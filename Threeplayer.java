    public void log(String log) {
        try {
            byte[] b = Files.readAllBytes(Paths.get("log.txt"));
            byte[] enter = Files.readAllBytes(Paths.get("enter.txt"));
            String str = new String(b, "UTF-8");
            String ent = new String(enter, "UTF-8");
            String newContent = str + log + ent;
            Files.write(Paths.get("log.txt"), newContent.getBytes());
        } catch (Exception exception) {
            msg(exception.toString());
        }
    }

    public void level() {
        int outcount = 0;
        if (player == 1 || player == 0) {
            outcount = boutcount;
            log("boutcount" + boutcount);
        } else if (player == 2) {
            outcount = routcount;
            log("routcount" + routcount);
        } else if (player == 3) {
            outcount = youtcount;
        }

        switch (s) {
            case 1:
                if (outcount > 0) {
                    insert = 1;
                    playerchance = 1;
                } else if (outcount == 0) {
                    move = 1;
                    playerchance = 1;
                }
                break;
            case 2:
                insert = 0;
                if (outcount < 4) {
                    move = 1;
                    playerchance = 1;
                }
                break;
            case 3:
                if (outcount < 4) {
                    move = 1;
                    playerchance = 1;
                }
                break;
            case 4:
                if (outcount > 0) {
                    insert = 1;
                    playerchance = 2;
                } else if (outcount == 0) {
                    move = 1;

                    playerchance = 2;
                }
                break;
            case 8:
                if (outcount == 0) {
                    move = 1;
                    playerchance = 2;
                    insert = 0;

                } else if (outcount == 1) {
                    insert = 1;
                    move = 1;
                    playerchance = 3;
                    s = 4;
                } else if (outcount > 1) {
                    insert = 2;
                    move = 0;
                    playerchance = 3;
                }
                break;
            default:
                break;

        }
    }

